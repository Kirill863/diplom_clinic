
"""
Конфигурация приложения Core для Django проекта клиники.

Модуль содержит класс конфигурации приложения, который определяет
метаданные и настройки основного приложения системы управления клиникой.
"""

from django.apps import AppConfig


class CoreConfig(AppConfig):
    """
    Класс конфигурации основного приложения клиники.
    
    Определяет базовые настройки приложения Core, включая:
    - Тип автоматического поля для моделей
    - Название приложения
    - Метку приложения для внутренней идентификации
    
    Attributes:
        default_auto_field (str): Тип поля для автоматического создания 
                                первичных ключей в моделях. Используется
                                BigAutoField для поддержки больших значений.
        name (str): Полное Python-путь к приложению (django.apps).
        label (str): Короткое уникальное имя приложения для использования
                    в конфигурациях и миграциях.
    """
    
    # Тип автоматического поля для первичных ключей
    default_auto_field = 'django.db.models.BigAutoField'
    
    # Полное Python-путь к приложению
    name = 'core'
    
    # Короткое имя приложения для использования в проекте
    label = 'core'
